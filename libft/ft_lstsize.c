#include "libft.h"

/**
 * @brief The ft_lstsize funtion counts the number of elements in a list.
 * 
 * @param lst 
 * @return int 
 */
int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 0;
	if (!lst)
		return (0);
	while (lst)
	{
		lst = lst->next;
		i++;
	}
	return (i);
}
