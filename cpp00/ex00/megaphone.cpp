#include <iostream>
#include <cctype>

int main(int argc, char **argv)
{
	int i;

	if (argc == 1)
	{
		std::cout << "* LOUD AND UNBEARABLE FEEDBACK NOISE *" << std::endl;
		return (0);
	}
	for (i = 0; argv[1][i]; i++)
		std::cout << (char)toupper(argv[1][i]);
	std::cout << std::endl;
	return (0);
}