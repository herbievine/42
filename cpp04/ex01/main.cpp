#include "Animal.hpp"
#include "Dog.hpp"
#include "Cat.hpp"
#include "WrongAnimal.hpp"
#include "WrongCat.hpp"
#include <iostream>

#define MAX_ANIMALS 2

int main(void)
{
	const Animal *animals[MAX_ANIMALS];

	for (int i = 0; i < MAX_ANIMALS; i++)
	{
		if (i % 2 == 0)
			animals[i] = new Dog();
		else
			animals[i] = new Cat();
	}

	for (int i = 0; i < MAX_ANIMALS; i++)
		delete animals[i];

	return 0;
}
